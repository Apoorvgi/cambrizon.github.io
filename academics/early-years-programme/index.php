<li>Personal, social and emotional development</li>
<li>Literacy</li>
<li>Mathematics</li>
<li>Understanding the world</li>
<li>Expressive arts and design</li>
</ul>
<p>Cambrizon’s International home-like care, eases the student into a learning mode, and the EYFS principles lays the groundwork for continual and effective discovery of self and surroundings. We regard every child as a unique individual with varied skills, capabilities and interests. We create an enabling environment and build positive relationships that allow them to explore these interests at their own pace.</p>
