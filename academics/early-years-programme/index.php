<p>Pre-primary education is most effective when the curriculum is delivered in a fun and engaging format. To achieve this, we follow UK’s leading Learning and Development model — the Early Years Foundation Stage (EYFS) framework which fits comfortably into the NCERT curriculum guideline.</p>
<p>Creating an environment where children are encouraged to learn through exploration and develop critical thinking is the key objective of this framework. Through active games and play, we try to identify the multiple intelligence of individual students and guide them through their personal learning styles. We facilitate them to explore the seven key areas of development:</p>
<ul class="o-list">
<li>Language and communication</li>
